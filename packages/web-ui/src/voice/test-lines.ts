/**
 * Returns a random test line, avoiding repeating the last one if provided.
 */
export function randomTestLine(lastLine?: string): string {
  if (TEST_LINES.length === 0) return 'Hello. Voice test complete.';
  if (TEST_LINES.length === 1) return TEST_LINES[0]!;
  let line: string;
  do {
    line = TEST_LINES[Math.floor(Math.random() * TEST_LINES.length)]!;
  } while (line === lastLine);
  return line;
}
