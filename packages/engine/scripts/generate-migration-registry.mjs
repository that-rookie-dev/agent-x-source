/**
 * Generate src/db/migration-registry.ts from SQL files in src/db/migrations/.
 *
 * This script reads all .sql files in the migrations directory, sorts them by
 * version number, and emits a TypeScript file that embeds the SQL content as
 * template string literals. This ensures the migrations are bundled into the
 * app by tsup/esbuild without requiring runtime file system access.
 *
 * Run this script before `tsup` during the build:
 *   node scripts/generate-migration-registry.mjs
 *
 * To add a new migration:
 *   1. Create a new file: src/db/migrations/V006__descriptive_name.sql
 *   2. Re-run this script (or the build, which runs it automatically)
 */
import { readFileSync, readdirSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const engineDir = join(scriptDir, '..');
const migrationsDir = join(engineDir, 'src', 'db', 'migrations');
const outputFile = join(engineDir, 'src', 'db', 'migration-registry.ts');

function parseVersion(filename) {
  const match = filename.match(/^(?:V)?(\d+)/i);
  return match ? parseInt(match[1], 10) : null;
}

function parseName(filename) {
  const base = filename.replace(/\.sql$/i, '');
  const parts = base.split(/__|_/).filter(Boolean);
  if (parts.length > 0 && /^(?:V)?\d+$/i.test(parts[0])) {
    return parts.slice(1).join('_');
  }
  return base;
}

function escapeSql(sql) {
  // Escape backticks and ${} sequences for safe embedding in a TS template literal
  return sql
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');
}

if (!existsSync(migrationsDir)) {
  console.error('Migration directory not found:', migrationsDir);
  process.exit(1);
}

const files = readdirSync(migrationsDir)
  .filter((f) => f.endsWith('.sql'))
  .map((filename) => {
    const version = parseVersion(filename);
    if (version === null) {
      console.warn(`Skipping non-versioned file: ${filename}`);
      return null;
    }
    const name = parseName(filename);
    const sql = readFileSync(join(migrationsDir, filename), 'utf-8');
    return { filename, version, name, sql };
  })
  .filter(Boolean)
  .sort((a, b) => a.version - b.version);

const lines = [
  '/**',
  ' * Auto-generated migration registry.',
  ' *',
  ' * This file is generated by scripts/generate-migration-registry.mjs from the',
  ' * SQL files in src/db/migrations/. Do not edit manually — add new .sql files',
  ' * to the migrations directory and re-run the generator.',
  ' *',
  ' * Each migration is embedded as a string literal so it gets bundled into the',
  ' * app by tsup/esbuild without needing runtime file access.',
  ' */',
  'import type { MigrationFile } from \'./MigrationRunner.js\';',
  '',
  'export const MIGRATION_FILES: MigrationFile[] = [',
];

for (const f of files) {
  lines.push(`  { version: ${f.version}, name: '${f.name}', sql: \`${escapeSql(f.sql)}\` },`);
}

lines.push('];');
lines.push('');

writeFileSync(outputFile, lines.join('\n'));
console.log(`Generated ${outputFile} with ${files.length} migration(s):`);
for (const f of files) {
  console.log(`  V${String(f.version).padStart(3, '0')}__${f.name}`);
}
